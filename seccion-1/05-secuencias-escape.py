# Agregar un caracter especial a un string

# ejemplo1
ejemplo1 = 'Ultimate "Python"'
print(ejemplo1)

# ejemplo2
ejemplo2 = "Ultimate \"Python\""
print(ejemplo2)


# ejemplo3
ejemplo3 = "Ultimatee \"\\Python\"\\"
print(ejemplo3)

# ejemplo 4 (Salto de linea)
ejemplo4 = "Ultimate\nPython"
print(ejemplo4)


# ejemplo 4 (Tabulacion)
ejemplo5 = "Ultimate\tPython"
print(ejemplo5)