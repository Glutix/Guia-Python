import math

# redondeo
print("\n --------- Redondear un numero ---------")
print(round(1.4))
print(round(1.7))
print("")

# Valor absoluto
print("valor absoluto de 3 > ", abs(3))
print("valor absoluto de -3 > ", abs(-3))
print("")

# Modulo Math
print("ceil > ", math.ceil(1.1))  # redondea hacia arriba, al entero mas proximo
print("floor > ", math.floor(1.9999))  # trunca la parte decimal
print("pow > ", math.pow(2, 3))  # potencia
print("sqrt > ", math.sqrt(9))  # raiz cuadrada

